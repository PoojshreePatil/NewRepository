package gettersetter;

public class Account {

	private int  id,accno,balance;

	public void setId(int id)
	{
		this.id = id;
	}
	public int getId()
	{
		return id;
	}
	
	public void setAccno(int accno	)
	{
		this.accno = accno;
	}
	public int getAccno()
	{
		return accno;
	}
	
	public void setBalance(int balance)
	{
		this.balance = balance;
	}
	public int getBalance()
	{
		return balance;
	}
}
